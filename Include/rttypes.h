#ifndef _RTTYPES_H_
#define _RTTYPES_H_

typedef void            T_VOID;         // void

typedef unsigned char   T_UBYTE;        // 8bit unsigned
typedef signed char     T_SBYTE;        // 8bit signed

typedef unsigned short  T_UWORD;        // 16bit unsigned
typedef signed short    T_SWORD;        // 16bit signed

typedef unsigned int    T_ULONG;        // 32bit unsigned
typedef signed int      T_SLONG;        // 32bit signed

typedef float           T_REAL;         // 32bit float
typedef float           T_REAL32;       // 32bit float
typedef double          T_REAL64;       // 64bit float

typedef char            T_CHAR;         // char type
typedef char            T_BOOL;         // bool variable

#endif
